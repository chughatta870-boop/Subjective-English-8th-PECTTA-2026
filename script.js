/* ---------------- APP STATE / RENDER ---------------- */
let activeSection = "applications";
let searchTerm = "";
let revealedKeys = new Set();

function wordCount(text) {
  return text.trim().split(/\s+/).length;
}

function highlightMatch(text, term) {
  if (!term) return escapeHtml(text);
  const escaped = escapeHtml(text);
  const safeTerm = term.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  return escaped.replace(new RegExp(`(${safeTerm})`, "ig"), "<mark>$1</mark>");
}

function escapeHtml(str) {
  return str.replace(/[&<>"']/g, (c) => ({
    "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;"
  }[c]));
}

function renderTabs() {
  const tabsEl = document.getElementById("tabs");
  tabsEl.innerHTML = SECTIONS.map(s => `
    <button class="tab ${s.key === activeSection ? "active" : ""}" data-key="${s.key}">
      <span class="tab-icon">${s.icon}</span>${s.label}
    </button>
  `).join("");
  tabsEl.querySelectorAll(".tab").forEach(btn => {
    btn.addEventListener("click", () => {
      activeSection = btn.dataset.key;
      document.getElementById("search").value = "";
      searchTerm = "";
      renderTabs();
      renderContent();
    });
  });
}

function matchesSearch(item) {
  if (!searchTerm) return true;
  const t = searchTerm.toLowerCase();
  if (item.title.toLowerCase().includes(t)) return true;
  if (item.body && item.body.toLowerCase().includes(t)) return true;
  if (item.passage && item.passage.toLowerCase().includes(t)) return true;
  return false;
}

function renderContent() {
  const section = SECTIONS.find(s => s.key === activeSection);
  const container = document.getElementById("content");
  const items = section.data.filter(matchesSearch);
  document.getElementById("resultCount").textContent =
    `${items.length} of ${section.data.length} item${section.data.length > 1 ? "s" : ""}`;

  if (items.length === 0) {
    container.innerHTML = `<div class="empty">No results for "${escapeHtml(searchTerm)}" in ${section.label}.</div>`;
    return;
  }

  if (section.type === "comp") {
    container.innerHTML = items.map(item => renderComprehensionCard(item)).join("");
  } else {
    container.innerHTML = items.map(item => renderCard(item)).join("");
  }

  container.querySelectorAll(".copy-btn").forEach(btn => {
    btn.addEventListener("click", () => copyCard(btn.dataset.id));
  });
  container.querySelectorAll(".reveal-btn").forEach(btn => {
    btn.addEventListener("click", () => {
      const id = btn.dataset.id;
      if (revealedKeys.has(id)) revealedKeys.delete(id); else revealedKeys.add(id);
      renderContent();
    });
  });
}

function renderCard(item) {
  return `
    <article class="card" id="${item.id}">
      <div class="card-head">
        <h2>${highlightMatch(item.title, searchTerm)}</h2>
        <span class="badge">${wordCount(item.body)} words</span>
      </div>
      <pre class="card-body">${highlightMatch(item.body, searchTerm)}</pre>
      <div class="card-actions">
        <button class="copy-btn" data-id="${item.id}">Copy text</button>
      </div>
    </article>
  `;
}

function renderComprehensionCard(item) {
  const revealed = revealedKeys.has(item.id);
  const qas = item.questions.map((qa, i) => `
    <li class="qa">
      <p class="q"><strong>Q${i + 1}.</strong> ${highlightMatch(qa.q, searchTerm)}</p>
      ${revealed ? `<p class="a">${highlightMatch(qa.a, searchTerm)}</p>` : ""}
    </li>
  `).join("");

  return `
    <article class="card comp-card" id="${item.id}">
      <div class="card-head">
        <h2>${highlightMatch(item.title, searchTerm)}</h2>
        <span class="badge">${wordCount(item.passage)} words</span>
      </div>
      <p class="passage">${highlightMatch(item.passage, searchTerm)}</p>
      <h3 class="qa-heading">Questions</h3>
      <ol class="qa-list">${qas}</ol>
      <div class="card-actions">
        <button class="reveal-btn" data-id="${item.id}">${revealed ? "Hide answer key" : "Show answer key"}</button>
      </div>
    </article>
  `;
}

function copyCard(id) {
  const section = SECTIONS.find(s => s.data.some(i => i.id === id));
  const item = section.data.find(i => i.id === id);
  const text = item.body;
  navigator.clipboard.writeText(text).then(() => {
    const btn = document.querySelector(`[data-id="${id}"].copy-btn`);
    if (btn) {
      const original = btn.textContent;
      btn.textContent = "Copied!";
      setTimeout(() => (btn.textContent = original), 1200);
    }
  });
}

/* ---------------- INIT ---------------- */
document.addEventListener("DOMContentLoaded", () => {
  const params = new URLSearchParams(window.location.search);
  const requestedSection = params.get("section");
  if (requestedSection && SECTIONS.some(s => s.key === requestedSection)) {
    activeSection = requestedSection;
  }

  renderTabs();
  renderContent();

  document.getElementById("search").addEventListener("input", (e) => {
    searchTerm = e.target.value;
    renderContent();
  });

  document.getElementById("printBtn").addEventListener("click", () => window.print());

  // Register service worker
  if ("serviceWorker" in navigator) {
    window.addEventListener("load", () => {
      navigator.serviceWorker.register("./sw.js").catch(() => {});
    });
  }

  // Install prompt handling
  let deferredPrompt = null;
  const installBtn = document.getElementById("installBtn");
  window.addEventListener("beforeinstallprompt", (e) => {
    e.preventDefault();
    deferredPrompt = e;
    installBtn.hidden = false;
  });
  installBtn.addEventListener("click", async () => {
    if (!deferredPrompt) return;
    deferredPrompt.prompt();
    await deferredPrompt.userChoice;
    deferredPrompt = null;
    installBtn.hidden = true;
  });
  window.addEventListener("appinstalled", () => {
    installBtn.hidden = true;
  });
});
